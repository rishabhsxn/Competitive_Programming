/*  first sort the given array
    select the element at index zero (x) and run an inner loop to check if the next
    element is equal to x or x+1, if yes, increase the count, else select the other
    DISTINCT elements and start the next loop after comparing for count.
 */

int pickingNumbers(int a_count, int* a)
{
    mergeSort(a, 0, a_count);

    int i,j,count=0,count0=0,x,y=0;

    for(i=0;i<a_count-1;i++)
    {
      x= *(a+i);
      count=1;
      for(j=i+1; j<a_count;j++)
      {
        if(*(a+j)==x || *(a+j)== x+1)
        {
          if(*(a+j)== x+1 && y==0)
            {
              i=j;
              y=1;
            }
          count++;
        }
        else
          break;

      }

      if(count>count0)
        count0=count;
    }
    return count0;
}


void merge(int arr[], int l, int m, int r)
{
    int i, j, k;
    int n1 = m - l + 1;
    int n2 =  r - m;

    /* create temp arrays */
    int L[n1], R[n2];

    /* Copy data to temp arrays L[] and R[] */
    for (i = 0; i < n1; i++)
        L[i] = arr[l + i];
    for (j = 0; j < n2; j++)
        R[j] = arr[m + 1+ j];

    /* Merge the temp arrays back into arr[l..r]*/
    i = 0; // Initial index of first subarray
    j = 0; // Initial index of second subarray
    k = l; // Initial index of merged subarray
    while (i < n1 && j < n2)
    {
        if (L[i] <= R[j])
        {
            arr[k] = L[i];
            i++;
        }
        else
        {
            arr[k] = R[j];
            j++;
        }
        k++;
    }

    /* Copy the remaining elements of L[], if there
       are any */
    while (i < n1)
    {
        arr[k] = L[i];
        i++;
        k++;
    }

    /* Copy the remaining elements of R[], if there
       are any */
    while (j < n2)
    {
        arr[k] = R[j];
        j++;
        k++;
    }
}

/* l is for left index and r is right index of the
   sub-array of arr to be sorted */
void mergeSort(int arr[], int l, int r)
{
    if (l < r)
    {
        // Same as (l+r)/2, but avoids overflow for
        // large l and h
        int m = l+(r-l)/2;

        // Sort first and second halves
        mergeSort(arr, l, m);
        mergeSort(arr, m+1, r);

        merge(arr, l, m, r);
    }
}

